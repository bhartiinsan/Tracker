# | Topic                | Meaning                           | Example            |
# | -------------------- | --------------------------------- | ------------------ |
# | Set                  | Unique, unordered collection      | s = {10, 20, 30}   |
# | add()                | Adds one item                     | s.add(70)          |
# | update()             | Adds many items                   | s.update([80, 90]) |
# | remove()             | Removes item, error if missing    | s.remove(20)       |
# | discard()            | Removes item, no error if missing | s.discard(100)     |
# | pop()                | Removes any one item              | s.pop()            |
# | clear()              | Removes all items                 | s.clear()          |
# | Union                | All unique values from both sets  | s \| t             |
# | Intersection         | Common values only                | s & t              |
# | Difference           | Values in first set only          | s - t              |
# | Symmetric difference | Values not common                 | s ^ t              |

